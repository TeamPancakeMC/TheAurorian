package cn.teampancake.theaurorian.common.enchantments;

import net.minecraft.world.item.enchantment.Enchantment;
import net.minecraft.world.item.enchantment.EnchantmentCategory;
import net.minecraft.world.item.enchantment.Enchantments;

public class AmnesiaCurseEnchantment extends Enchantment {

    public AmnesiaCurseEnchantment() {
        super(Rarity.RARE, EnchantmentCategory.ARMOR, Enchantments.ARMOR_SLOTS);
    }

    @Override
    public int getMinCost(int pEnchantmentLevel) {
        return 25;
    }

    @Override
    public int getMaxCost(int pEnchantmentLevel) {
        return 50;
    }

    @Override
    public boolean isTreasureOnly() {
        return true;
    }

    @Override
    public boolean isCurse() {
        return true;
    }

}